#include <stdio.h>

int main() {
    int number;
    number = 10;
    printf("The current number is %d.\n", number);
    number += 100;
    printf("The current number is %d.\n", number);
    number -= 50;
    printf("The current number is %d.\n", number);
    number *= 3;
    printf("The current number is %d.\n", number);
    return 0;
}
